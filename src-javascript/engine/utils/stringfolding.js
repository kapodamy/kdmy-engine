function string_to_uppercase(str) {
    if (str == null) return null;
    if (str.length < 1) return "";

    const grapheme = { code: 0, size: 0 };
    const stringbuilder = stringbuilder_init(str.length);

    let index = 0;
    while (string_get_character_codepoint(str, index, grapheme)) {
        index += grapheme.size;

        L_find_lowercase_components: {
            for (let i = 0; STRING_LOWERCASE_TO_COMPONENTS[i][0]; i++) {
                const lowercase_components = STRING_LOWERCASE_TO_COMPONENTS[i];
                if (lowercase_components[0] != grapheme.code) continue;

                // decompose components found, unfold each one
                for (let j = 1; j < lowercase_components.length; j++) {
                    string_internal_direct_uppercase_conversion(lowercase_components[j], stringbuilder);
                }

                break L_find_lowercase_components;
            }

            // simple character unfolding
            string_internal_direct_uppercase_conversion(grapheme.code, stringbuilder);
        }
    }

    return stringbuilder_finalize(stringbuilder);
}

function string_to_lowercase(str) {
    if (str == null) return null;
    if (str.length < 1) return "";

    const grapheme = { code: 0, size: 0 };
    const stringbuilder = stringbuilder_init(str.length);

    let index = 0;
    while (string_get_character_codepoint(str, index, grapheme)) {
        index += grapheme.size;

        L_find_in_uppercase_list: {
            for (let i = 0; STRING_UPPERCASES[i]; i++) {
                if (STRING_UPPERCASES[i] == grapheme.code) {
                    stringbuilder_add_char_codepoint(stringbuilder, STRING_LOWERCASES[i]);
                    break L_find_in_uppercase_list;
                }
            }

            // if not found, the codepoint was a number, symbol or emoji
            stringbuilder_add_char_codepoint(stringbuilder, grapheme.code);
        }
    }

    return stringbuilder_finalize(stringbuilder);
}


function string_internal_direct_uppercase_conversion(codepoint, stringbuilder) {
    for (let i = 0; STRING_LOWERCASES[i]; i++) {
        if (STRING_LOWERCASES[i] == codepoint) {
            codepoint = STRING_UPPERCASES[i];
            break;
        }
    }

    // if not found, the codepoint was a number, symbol or emoji
    stringbuilder_add_char_codepoint(stringbuilder, codepoint);
}

